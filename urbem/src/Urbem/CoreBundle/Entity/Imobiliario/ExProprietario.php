     *
     * @param integer $ordem
     * @return ExProprietario
     */
    public function setOrdem($ordem)
    {
        $this->ordem = $ordem;
        return $this;
    }

    /**
     * Get ordem
     *
     * @return integer
     */
    public function getOrdem()
    {
        return $this->ordem;
    }

    /**
     * Set cota
     *
     * @param integer $cota
     * @return ExProprietario
     */
    public function setCota($cota)
    {
        $this->cota = $cota;
        return $this;
    }

    /**
     * Get cota
     *
     * @return integer
     */
    public function getCota()
    {
        return $this->cota;
    }

